export async function createLargePdf(): Promise<Buffer> {
  const pdfBuffer = await createMinimalPdf();
  const largeBuffer = Buffer.alloc(5 * 1024 * 1024 + 1, pdfBuffer[0]);
  return largeBuffer;
}

