export function detectFileType(
  buf: Buffer,
): { ext: string; mime: string } | undefined {
  if (buf[0] === 0x25 && buf[1] === 0x50)
    return { ext: "pdf", mime: "application/pdf" };
  if (buf[0] === 0x89 && buf[2] === 0x4e)
    return { ext: "png", mime: "image/png" };
  if (buf[0] === 0xff && buf[1] === 0xd8)
    return { ext: "jpg", mime: "image/jpeg" };
  if (buf[0] === 0x47 && buf[1] === 0x49 && buf[2] === 0x46)
    return { ext: "gif", mime: "image/gif" };
  return undefined;
}

export function createTempFile(name: string, content: string): string {
  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), "vao-documents-"));
  const filePath = path.join(tmpDir, name);
  fs.writeFileSync(filePath, content);
  return filePath;
}
